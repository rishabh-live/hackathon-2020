    <div class="container">

      <p id="p1"   >The first view of Sikkim Manipal Institute of Technology (SMIT) is awe inspiring and more than enough to motivate any one to be a part of this beautiful, resort like campus nestling on the banks of River Teesta in the verdant hills of East Sikkim. The institute is a constituent college of Sikkim Manipal University located in Tadong, Gangtok.  Established in 1997, SMIT is part of the renowned Manipal Group that has made its mark in the field of education and health services in India and abroad. SMIT enjoys approval by All India Council of Technical Education (AICTE) and University Grants Commission (UGC). It is also accredited by NBA. In addition, it also holds ISO 9001 accreditation vide NS-EN ISO 9001:2000.</p>
      <h2>Infrastructure</h2>
      <p id='p2' >
      SMIT is one of Indiaâ€™s premier technology and management institutes. Spread across 35 beautifully landscaped acres, the campus is home to over 3200 students, 220 members of faculty  and a large and efficient team of support staff. The institute is reputed for its state of the art infrastructure, well stocked air-conditioned library, advanced technology laboratories, a sports complex with swimming pool, food court, separate boys and girls hostels with rooms having attached baths, 24 hours running water with hot water facilities for winters, night canteens inside hostels, a electrical sub-station for 24 hour generator support etc. . All these facilities and amenities have made SMIT one of the best and most sought after engineering and management institutes in the nation.
      <h3>Academics</h3>
     </p>
     <p id='p3' >
      The academic standard of SMIT is at par with the best institutes in the country. A highly qualified and dedicated faculty ensure that the students are provided with a holistic education that makes them job ready, industry ready and most important, life ready. The syllabus followed is prepared in consultation with industry experts and leading academicians from premier institutes like IIT Kgp, Jadavpur University, TAPMI etc. The course content in revised every three years to meet the changing demands of the industry. It is not without reason that our glittering alumni are placed with reputed organizations like Microsoft, Oracle, SAP Laps, TCS and also pursue higher studies from IIT's and IIM's.
      Faculty : Student = 1 : 15
     </p>
     <h1 id=hd1>Research</h1>
     <p id="p3">
      Research is the life blood of a sound academic institute.  SMIT takes pride in the fact that it has emerged as a research friendly institute. A number of scholars have been awarded with PhD and many more scholars from within and outside the institute are registered for doctorate. A number of sponsored projects have also been assigned to SMIT. The faculty members are also selected to do research on about 17 sponsored projects approved and financed by DST, DBT,DRDO and AICTE. As a result, apart from books, a number of papers by the members of the faculty are also published in peer reviewed, high index journals of international repute. The students are also guided and encouraged to participate in research activities. <a href="https://smu.edu.in/smu/academics/institution-list/smit/research-smit.html">(Research@SMIT- click here)</a>
     </p>
     <h4>Courses</h4>
     <p id="p4" >
      Sikkim Manipal Institute of Technology offers a range of courses in Engineering, Basic Sciences and Management Studies. The courses offered are as follows:

      BTech in Civil Engineering, Computer Science Engineering, Electrical and Electronic Engineering, Electronic and Communication Engineering, Information Technology, Mechanical Engineering, .
      MTech in almost all branches given above
      BBA, MBA with specialization in Marketing , Finance, Human Resources and Systems
      BCA, MCA
      MSc in Applied Sciences: Physics, Chemistry, Mathematics
      Centre for Material Science and Nano Technology
      PhD in all streams
     </p>
     <h5>Placements</h5>
     <p id="p5" >
        SMIT enjoys a strong student placement record. SMIT students have been placed in well-known national and international organizations such as, Microsoft, FL Smidth, TCS, Infosys, Wells Fargo, Cognizant, ANZ, Wipro, HCL, Tech Mahindra, Accenture, Satyam, Microland, NIIT, Tata Motors, Tractor India Ltd., Hindustan Motors, Skytech, Ushacom, Pricewaterhouse Coopers, Tata Elxsi, Hutch, ANZ, IDBI,HDFC (Mutual funds), Convergys, Times of India and many other reputed and prestigious organizations. SMIT is poised to attain even greater heights in times to come. The untiring effort of each and every member of the staff and the students to achieve that greatness in life will definitely see SMIT growing and moving from one milestone to the next with glory and success.
     </p>
     <h6>Education Beyond Academics</h6>
